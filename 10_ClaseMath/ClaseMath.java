
public class ClaseMath {
    public static void main(String[] args) {
        System.out.println("CLASE MATH\n");

        System.out.println("Las bases...\n");

        System.out.println(" Math.PI = " + Math.PI);
        System.out.println(" Math.E = " + Math.E + '\n');

        System.out.println(" Math.ceil(4.5) = " + Math.ceil(4.5));
        System.out.println(" Math.floor(4.5) = " + Math.floor(4.5));
        System.out.println(" Math.round(9.46) = " + Math.round(9.46) + '\n');

        System.out.println(" Math.abs(-8) = " + Math.abs(-8));
        System.out.println(" Math.max(5.23, -3.1011) = " + Math.max(5.23, -3.1011) + '\n');

        System.out.println(" Math.log(e⁷) = " + Math.log(Math.exp(7)));
        System.out.println(" Math.sqrt(7) = " + Math.sqrt(7));
        System.out.println(" Math.pow(2, 10) = " + Math.pow(2, 10) + '\n');

        System.out.println("Trogonometríaaa\n");

        System.out.println(" Math.toDegrees(\u03C0/4) = " + Math.toDegrees(Math.PI/4));
        System.out.println(" Math.toRadians(180) = " + Math.toRadians(180) + '\n');

        System.out.println(" Math.sin(-\u03C0/2) = " + Math.sin(-Math.PI/2));
        System.out.println(" Math.cos(-\u03C0/2) = " + Math.cos(-Math.PI/2));
    }
}
